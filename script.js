// CART 1


var audio1 = document.getElementById("myAudio1");

function play1() {

    // AUDIO WALAA LANJAAAAAA

    if (audio1.paused) {
        audio1.play();
    } else {
        audio1.pause();
    }

    // BUTTON KI APEARANCE KA LANJAAAA

    var Z = document.getElementById('PAUSE1');
    if (Z.style.display === 'block') {
        Z.style.display = 'none';
    } else {
        Z.style.display = 'block';
    }

    var X = document.getElementById('PLAY1');
    if (X.style.display === 'none') {
        X.style.display = 'block';
    } else {
        X.style.display = 'none';
    }
}

